// Tagall
export const TAGALL_PHRASE_RE = /и другие участники задачи/i;
export const TAGALL_WORD_RE = /\bTAGALL\b/gi;

// Bitrix разворачивает TAGALL в "[Имя Фамилия] и другие участники задачи".
// Имя перед фразой опционально и поглощается заменой, чтобы не считаться личным упоминанием.
// Глобальный флаг — для замены всех вхождений (канонизация текста уведомления).
export const TAGALL_NAMED_RE = new RegExp(`(?:[А-ЯЁ][а-яё]+ [А-ЯЁ][а-яё]+ )?${TAGALL_PHRASE_RE.source}`, 'g');

export const TAGALL_STATUS_KEYWORDS = [
  'на прод[е]?',
  'в прод[е]?',
  'прод[е]?',
  'готов[оа]?',
  'сделал[аи]?',
  'сделано',
  'выкатил[аи]?',
  'задеплоил[аи]?',
  'задеплоен[оа]?',
  'задеплой',
  'деплой',
  'выпустил[аи]?',
  'запустил[аи]?',
  'опубликовал[аи]?',
  'завершил[аи]?',
  'завершен[оа]?',
  'выполнил[аи]?',
  'выполнен[оа]?',
  'закончил[аи]?',
  'закончен[оа]?',
  'исправил[аи]?',
  'исправлен[оа]?',
  'пофиксил[аи]?',
  'фикс',
  'запушил[аи]?',
  'зарелизил[аи]?',
  'релиз',
  'ок',
  'ok',
  'done',
  'ready',
  'fixed',
];

// Проверяется на канонизированном тексте, где tagall-фраза заменена на TAGALL.
// Хвостовой (?!…) — граница слова с учётом кириллицы (JS \b после кириллических букв не
// срабатывает), чтобы слово не матчилось внутри длинного: «готово» да, «готовится» нет.
export const TAGALL_STATUS_RE = new RegExp(
  `TAGALL,?\\s*(${TAGALL_STATUS_KEYWORDS.join('|')})(?![0-9A-Za-zА-Яа-яЁё])`,
  'i',
);

// Цитата в уведомлении: "[Фамилия Имя] написал/а/и:" — атрибуция в блоке цитирования Bitrix
export const NOTIF_QUOTE_RE = /[А-ЯЁ][а-яё]+ [А-ЯЁ][а-яё]+ написал[а]?:/;

// Блок цитаты целиком: атрибуция + её содержимое (на той же строке или многострочное до пустой
// строки либо до конца текста). Завершающий \n опционален — иначе обрезанная превью-строка без
// переноса в конце не вырезалась бы, и имя из чужой цитаты ложно считалось бы упоминанием.
export const NOTIF_QUOTE_STRIP_RE = /[А-ЯЁ][а-яё]+ [А-ЯЁ][а-яё]+ написал[а]?:(?:[^\n]+|\n(?:[^\n]+\n?)*)/g;

// Типы уведомлений (для чипа в notification-details)
export const NOTIF_NEW_TASK_RE = /добавил[а]? новую задачу|добавлена новая задача/i;
export const NOTIF_COMMENT_RE = /добавил[а]? комментарий|добавлен комментарий/i;
export const NOTIF_REACTION_RE = /отреагировал(\(а\)|а)? на (?:ваш )?комментарий|поставил[а]? реакцию|благодарит вас в сообщении/i;
export const NOTIF_CLOSE_RE = /закрыл[а]? задачу|закрыта задача/i;
export const NOTIF_CHANGE_RE = /изменил[а]? задачу|изменена задача/i;
