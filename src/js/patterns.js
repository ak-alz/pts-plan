// BBCode → Markdown (см. bbcodeToMarkdown в utils.js). Набор тегов — по официальной справке
// Bitrix24 (форматирование текста задач/комментариев), а не по BBCode-спеке в целом.
// Атрибуты тега в реальных данных Bitrix встречаются и через "=" ([SPOILER=title]), и через
// пробел ([IMG WIDTH=432 HEIGHT=325]) — там, где имя тега фиксировано литералом (не общий
// фолбэк), после имени тега допускаем любые символы до "]", а не только "=значение".
