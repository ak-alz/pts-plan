// COLOR/SIZE — оформление без аналога в Markdown, обёртка снимается, текст остаётся.
// LEFT/CENTER/RIGHT — выравнивание, тоже без аналога.
export const BBCODE_STRIP_WRAPPER_RE = /\[(COLOR|SIZE|LEFT|CENTER|RIGHT)[^\]]*\]([\s\S]*?)\[\/\1\]/gi;
// Фолбэк — любой оставшийся нераспознанный тег вида [XXX]/[/XXX=...]. Имя тега ограничено
// заглавной латиницей, чтобы не задеть кириллические плейсхолдеры типа "[Вложения: ...]".
export const BBCODE_UNKNOWN_TAG_RE = /\[\/?[A-Z][A-Z0-9]*(?:=[^\]]*)?\]/g;

