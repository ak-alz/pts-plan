// Bitrix разворачивает TAGALL в "[Имя Фамилия] и другие участники задачи".
// Имя перед фразой опционально и поглощается заменой, чтобы не считаться личным упоминанием.
// Глобальный флаг — для замены всех вхождений (канонизация текста уведомления).
export const TAGALL_NAMED_RE = new RegExp(`(?:[А-ЯЁ][а-яё]+ [А-ЯЁ][а-яё]+ )?${TAGALL_PHRASE_RE.source}`, 'g');

