// Проверяется на канонизированном тексте, где tagall-фраза заменена на TAGALL.
// Хвостовой (?!…) — граница слова с учётом кириллицы (JS \b после кириллических букв не
// срабатывает), чтобы слово не матчилось внутри длинного: «готово» да, «готовится» нет.
export const TAGALL_STATUS_RE = new RegExp(
  `TAGALL,?\\s*(${TAGALL_STATUS_KEYWORDS.join('|')})(?![0-9A-Za-zА-Яа-яЁё])`,
  'i',
);

// Цитата в уведомлении: "[Фамилия Имя] написал/а/и:" — атрибуция в блоке цитирования Bitrix
export const NOTIF_QUOTE_RE = /[А-ЯЁ][а-яё]+ [А-ЯЁ][а-яё]+ написал[а]?:/;

// Блок цитаты целиком: атрибуция + её содержимое (на той же строке или многострочное до пустой
// строки либо до конца текста). Завершающий \n опционален — иначе обрезанная превью-строка без
// переноса в конце не вырезалась бы, и имя из чужой цитаты ложно считалось бы упоминанием.
export const NOTIF_QUOTE_STRIP_RE = /[А-ЯЁ][а-яё]+ [А-ЯЁ][а-яё]+ написал[а]?:(?:[^\n]+|\n(?:[^\n]+\n?)*)/g;

