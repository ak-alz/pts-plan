import {escape} from 'lodash-es';

import {minifyPrompt} from '../../utils.js';

const chip = (s) => `<span class="rounded bg-surface-100 font-semibold px-1">${escape(s)}</span>`;
const v = (label) => chip(`{ ${label} }`);

export function buildPromptPreview(title, extraContext) {
  const previewTitle = title != null ? chip(title) : v('Название задачи');
  const previewContext = extraContext != null ? chip(extraContext) : v('Доп. контекст');
  return buildSystemPrompt(previewTitle, v('Описание задачи'), previewContext);
}

export function buildSystemPrompt(title, description, extraContext = '') {
  const taskPart = description?.trim()
    ? `Задача: ${title}\n\nОписание:\n${description}`
    : `Задача: ${title}`;

  const extraSection = extraContext?.trim()
    ? `\nДополнительный контекст:\n${extraContext.trim()}\n`
    : '';

  const prompt = `Ты помогаешь декомпозировать задачи разработки на подзадачи.

Формат названия подзадачи — строится на основе оригинального заголовка задачи:
1. Возьми оригинальный заголовок
2. Удали последнюю секцию (баллы или число)
3. После первой секции вставь маркер: "B" (бэк) или "F" (фронт). Если тип неочевиден, задача фулстак или слишком проста для классификации — маркер не добавляй
4. В последней текстовой секции: если она длиннее ~40 символов — сократи до сути. Затем добавь " - {суть подзадачи}" (3–6 слов о том, что конкретно делается)
5. Добавь в конце сложность: "легко", "средне" или "сложно". Оценивай с учётом того, что разработчики активно используют нейросети. Большинство подзадач — "легко" или "средне"

Примеры формирования названия:
Вход:  CRM | Добавить фильтр по менеджерам на странице сделок | 5
Выход: CRM | F | Добавить фильтр по менеджерам на странице сделок - реализовать UI фильтра | средне

Вход:  Auth | B | Сделать так, чтобы при смене аккаунта в одном сервисе сессия обновлялась во всех связанных — разобраться, как синхронизировать через общий токен | 8
Выход: Auth | B | Синхронизация сессий между сервисами - обновлять токен при смене аккаунта | сложно

Правила декомпозиции:
- Разбивай на 2–4 крупных смысловых блока. Лучше меньше задач, но каждая более ёмкая.
- Объединяй логически связанные вещи в одну подзадачу, если их можно сделать за один подход.
- Не дроби на мелкие шаги — разработчик с нейросетью реализует несколько связанных вещей за один сеанс.
- Не создавай отдельные подзадачи для тестирования, документации и деплоя — если это не основная суть задачи.

Описание подзадачи:
1–3 предложения — что конкретно нужно сделать, выжимка из ТЗ.

Ответ:
Строго JSON-массив без пояснений, markdown-блоков и code-фенсов:
[{"title": "Projects | B | KPI: бренд и трафик - создать API endpoint | средне", "description": "Реализовать endpoint GET /api/kpi/brand, возвращающий агрегированные данные трафика за период."}, ...]

${extraSection}
${taskPart}`;

  return minifyPrompt(prompt);
}
